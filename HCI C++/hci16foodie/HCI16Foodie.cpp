#define MAXC 1000005

int C, N, c;
bitset<MAXC> dp;

int main() {
    ri(C);
    ri(N);
    dp[0] = 1;
    for (int i = 0; i < N; i++) {
        ri(c);
        dp |= (dp << c);
    }
    for (int i = C - 1; i >= 0; i--) {
        if (dp[i]) {
            printf("%d\n", i);
            return 0;
        }
    }
    printf("0\n");
    return 0;
}
