typedef unordered_map<int, int> umii;
typedef unordered_map<int, ll> umill;
typedef unordered_map<ll, int> umlli;

const llu MOD = (1LLU << 32) - 5;

int N;
llu n, ans;

int main() {
    ri(N);
    for (int i = 0; i < N; i++) {
        rllu(n);
        if (n >= MOD) {
            printf("0\n");
            continue;
        }
        ans = 1;
        for (int j = 2; j <= n; j++) {
            ans = (ans * j) % MOD;
        }
        printf("%llu\n", ans);
    }
    return 0;
}
